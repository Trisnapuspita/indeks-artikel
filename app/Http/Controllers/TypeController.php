     */
    public function store(Request $request)
    {
        $slug = str_slug($request->title, '_');
        //cek slug ngga kembar
        if(Type::where('slug', $slug)->first() != null)
            $slug = $slug . '-'.time();

        $types = Type::create([
            'title' => $request->title,
            'slug' => $slug,
            'user_id'=> Auth::user()->id
        ]);

        return redirect('types')->with('msg', 'berhasil ditambahkan');
    }

